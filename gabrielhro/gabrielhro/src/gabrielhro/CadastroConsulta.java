    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (txtConvenio.getText().trim().isEmpty()
                || medicoComboBox.getSelectedItem() == null
                || pacienteComboBox.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos");
        } else {
            Consulta con = new Consulta();

            con.setConvenio(txtConvenio.getText().trim());

            String item = (String) medicoComboBox.getSelectedItem();
            int idMed = Integer.parseInt(item.split("#")[0]);
            con.setIdMedico(idMed);

            String pacienteItem = (String) pacienteComboBox.getSelectedItem();
            int idPac = Integer.parseInt(pacienteItem.split("#")[0]);
            con.setIdPaciente(idPac);

            String msg;
            String idCon = txtId.getText().trim();
            if (idCon.isEmpty()) {
                msg = consultaDAO.cadastraConsulta(con);
                if (msg.equals("1")) {
                    msg = "Cadastro efetuado com sucesso";
                }
                JOptionPane.showMessageDialog(rootPane, msg);
            } else {
                con.setIdConsulta(Integer.parseInt(idCon));
                msg = consultaDAO.atualizaConsulta(con);
                if (msg.equals("1")) {
                    msg = "Alteração efetuada com sucesso";
                }
                JOptionPane.showMessageDialog(rootPane, msg);
            }

            this.limpaCampos();
            this.preencheTabela();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void tableConsultasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableConsultasMouseClicked
        // TODO add your handling code here:
        btnExcluir.setEnabled(true);

        medicoComboBox.setSelectedItem(tableConsultas.getValueAt(tableConsultas.getSelectedRow(), 1).toString());
        pacienteComboBox.setSelectedItem(tableConsultas.getValueAt(tableConsultas.getSelectedRow(), 2).toString());
        txtId.setText(tableConsultas.getValueAt(tableConsultas.getSelectedRow(), 0).toString());
        txtConvenio.setText(tableConsultas.getValueAt(tableConsultas.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_tableConsultasMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o registro?");
        if (resposta == 0) {
            String msg = consultaDAO.excluiConsulta(Integer.parseInt(txtId.getText()));
            if (msg.equals("1")) {
                msg = "Exclusão efetuada com sucesso!";
            }
            JOptionPane.showMessageDialog(rootPane, msg);
            this.limpaCampos();
            this.preencheTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

