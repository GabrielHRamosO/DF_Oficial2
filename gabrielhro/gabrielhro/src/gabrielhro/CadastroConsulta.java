    public void preencheTabela() {
        ArrayList consultas;
        DefaultTableModel tabela = (DefaultTableModel) tableConsultas.getModel();
        tabela.setRowCount(0);
        consultas = consultaDAO.listaConsultas();
        Iterator it = consultas.iterator();
        while (it.hasNext()) {
            ConsultaTabela consulta = (ConsultaTabela) it.next();
            tabela.addRow(new Object[]{consulta.getIdConsulta(), consulta.getMedico(), consulta.getPaciente(), consulta.getConvenio()});
        }
    }

    public void limpaCampos() {
        btnExcluir.setEnabled(false);

        txtId.setText("");
        txtConvenio.setText("");

        medicoComboBox.removeAllItems();
        ArrayList arrayMed = medicoDAO.listaMedicos();
        for (int counter = 0; counter < arrayMed.size(); counter++) {
            Medico med = (Medico) arrayMed.get(counter);
            medicoComboBox.addItem(Integer.toString(med.getIdMedico()) + "#" + med.getNome());
        }
        pacienteComboBox.removeAllItems();
        ArrayList arrayPac = pacienteDAO.listaPacientes();
        for (int counter = 0; counter < arrayPac.size(); counter++) {
            Paciente pac = (Paciente) arrayPac.get(counter);
            pacienteComboBox.addItem(Integer.toString(pac.getIdPaciente()) + "#" + pac.getNome());
        }
    }

